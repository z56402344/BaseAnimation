package com.duguang.baseanimation.ui.imitate.biaopan;

public class BiaoPanData {
	private String city;
	private String chengdu;
	private String tianqi;
	private int qiwen;
	private String tishi;
	private int zhiliang;

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getChengdu() {
		return chengdu;
	}

	public void setChengdu(String chengdu) {
		this.chengdu = chengdu;
	}

	public String getTianqi() {
		return tianqi;
	}

	public void setTianqi(String tianqi) {
		this.tianqi = tianqi;
	}

	public int getQiwen() {
		return qiwen;
	}

	public void setQiwen(int qiwen) {
		this.qiwen = qiwen;
	}

	public String getTishi() {
		return tishi;
	}

	public void setTishi(String tishi) {
		this.tishi = tishi;
	}

	public int getZhiliang() {
		return zhiliang;
	}

	public void setZhiliang(int zhiliang) {
		this.zhiliang = zhiliang;
	}

}
